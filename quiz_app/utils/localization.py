# Current language setting
_current_language = 'en'

def set_language(language_code: str):
    """
    Set the current language for the application

    Args:
        language_code: Language code ('en' or 'az')
    """
    global _current_language
    if language_code in TRANSLATIONS:
        _current_language = language_code
    else:
        print(f"[WARNING] Unsupported language code: {language_code}. Defaulting to 'en'")
        _current_language = 'en'

def get_language():
    """Get the current language code"""
    return _current_language

def t(key: str, **kwargs) -> str:
    """
    Translate a key to the current language

    Args:
        key: Translation key
        **kwargs: Optional formatting arguments for string interpolation

    Returns:
        Translated string
    """
    translation = TRANSLATIONS.get(_current_language, {}).get(key, key)

    # Handle string interpolation if kwargs provided
    if kwargs:
        try:
            translation = translation.format(**kwargs)
        except KeyError as e:
            print(f"[WARNING] Missing placeholder in translation: {e}")

    return translation

def get_translations(language_code: str = None) -> dict:
    """
    Get all translations for a specific language

    Args:
        language_code: Language code (defaults to current language)

    Returns:
        Dictionary of translations
    """
    lang = language_code or _current_language
    return TRANSLATIONS.get(lang, TRANSLATIONS['en'])

