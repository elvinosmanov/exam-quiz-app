def get_department_abbreviation(department_name: str, language: str = None) -> str:
    """
    Get department abbreviation based on full name

    Args:
        department_name: Full department name (in either language)
        language: Language code ('en' or 'az'), defaults to current language

    Returns:
        Department abbreviation in the specified language, or original name if not found
    """
    from quiz_app.config import ORGANIZATIONAL_STRUCTURE, get_department_key

    lang = language or get_language()
    abbr_key = 'abbr_en' if lang == 'en' else 'abbr_az'

    # Get department key
    dept_key = get_department_key(department_name)
    if dept_key and dept_key in ORGANIZATIONAL_STRUCTURE:
        return ORGANIZATIONAL_STRUCTURE[dept_key].get(abbr_key, department_name)

    return department_name

def get_unit_abbreviation(department_name: str, unit_name: str, language: str = None) -> str:
    """
    Get unit abbreviation based on department and unit name

    Args:
        department_name: Full department name (in either language)
        unit_name: Full unit name (in either language)
        language: Language code ('en' or 'az'), defaults to current language

    Returns:
        Unit abbreviation in the specified language, or original name if not found
    """
    from quiz_app.config import ORGANIZATIONAL_STRUCTURE, get_department_key

    lang = language or get_language()
    abbr_key = 'abbr_en' if lang == 'en' else 'abbr_az'
    name_key = 'name_en' if lang == 'en' else 'name_az'

    # Get department key
    dept_key = get_department_key(department_name)
    if not dept_key or dept_key not in ORGANIZATIONAL_STRUCTURE:
        return unit_name

    dept_data = ORGANIZATIONAL_STRUCTURE[dept_key]

    # Check direct units under department
    for unit in dept_data.get('units', []):
        if unit.get('name_az') == unit_name or unit.get('name_en') == unit_name:
            return unit.get(abbr_key, unit_name)

    # Check units under sections
    for section_data in dept_data.get('sections', {}).values():
        for unit in section_data.get('units', []):
            if unit.get('name_az') == unit_name or unit.get('name_en') == unit_name:
                return unit.get(abbr_key, unit_name)

    return unit_name
